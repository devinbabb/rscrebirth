package org.rscdaemon.server.entityhandling.defs;

/**
 * The definition wrapper for Tiles
 * 
 * @author Devin
 */

public class TileDef {
	public int colour;
	public int unknown;
	public int objectType;

	public int getColour() {
		return colour;
	}

	public int getUnknown() {
		return unknown;
	}

	public int getObjectType() {
		return objectType;
	}
}